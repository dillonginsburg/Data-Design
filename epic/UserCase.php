<h1 align="center">How He Does It</h1>

<h2>Use Case:</h2>
	<p>As a Tesla enthusiast, Jonathon would like to create a community for his hobby.</p>
		<ul>
			<li>Goals: Create a subreddit for Tesla Hobbyists hosted by reddit.com.</li>
			<li>Preconditions: Jon is a power user of reddit. This means he has a reddit account active for 30+days, and has enough positive "karma" to create a community.</li>
			<li>Postconditions: Jonathon has created a forum and moderates it.</li>
</ul>




<h3>Interaction Flow</h3>
<ul>
	<li>Jonathon enters reddit.com into his browser.</li>
	<li>When the page loads, he logs in with his username and password.</li>
	<li>Because Mr. Tully is a valued member of the community and meets reddit's requirements for account age
		and established merit via 'karma points', he is allowed to click on a button called "Create Community" on
		his reddit hompage.</li>
	<li>A small window pops up, asking for the desired name of the new subreddit, whether you want access to be public or
		private, and a brief description of what the content of the new forum should be about. Jonathon fills these out accordingly, and hits 'create'.</li>
	<li>And then Jon is done. The forum is created, and a community built upon free access to technological
		education can develop around him.</li>
</ul>

